    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jCheckBox1.isSelected()){
            Mecanico.stablish(jTextField1.getText(), "", "", 0);//0 no bloqueo
            this.dispose();
            return;
        }
        String username = usu1.getText();
        if(!username.equals(usu2.getText())){
            JOptionPane.showMessageDialog(null, "Usuarios no coinciden");
            return;
        }

        String contra = p1.getText();
        if(!contra.equals(p2.getText())){
            JOptionPane.showMessageDialog(null, "Contrase침as no coinciden");
            return;
        }
        Mecanico.stablish(jTextField1.getText(), username, contra, 1);
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

